package calculadora;

public class Calculadora {

	//método soma	
	int soma(int a, int b) {
		int resultado = a + b;
		return resultado;
	}
	int subtracao(int a, int b) {
		int resultado = a - b;
		return resultado;
	}	
	int multiplicacao(int a, int b) {
		int resultado = a * b;
		return resultado;
	}	
	int divisao(int a, int b) {
		int resultado = a / b;
		return resultado;
	}
	}
	


