package calculadora;

public class Calculadora {

	void soma(int a, int b) {
		int resultado = a+b;
		System.out.printf("Resultado da soma: %d",resultado);
		
	}
	
	void subtração(int a, int b) {
		int resultado = a-b;
		System.out.printf("Resultado da subtração: %d",resultado);
		
	}
	
	void divisão(int a, int b) {
		int resultado = a/b;
		System.out.printf("Resultado da divisão: %d",resultado);
		
	}
	
	void multiplicação(int a, int b) {
		int resultado = a*b;
		System.out.printf("Resultado da multiplicação: %d",resultado);
		
	}
	
}


